package org.es.service.student.selectcourse;

public class SelectCourseCondition {
	private String courseNo;
	private String sno;
	private Integer scheNo;
	private String yeartime;
	private Integer termtime;
	private String tno;
	
	
	public String getTno() {
		return tno;
	}
	public void setTno(String tno) {
		this.tno = tno;
	}
	public String getYeartime() {
		return yeartime;
	}
	public void setYeartime(String yeartime) {
		this.yeartime = yeartime;
	}
	public Integer getTermtime() {
		return termtime;
	}
	public void setTermtime(Integer termtime) {
		this.termtime = termtime;
	}
	public String getCourseNo() {
		return courseNo;
	}
	public void setCourseNo(String courseNo) {
		this.courseNo = courseNo;
	}
	public String getSno() {
		return sno;
	}
	public void setSno(String sno) {
		this.sno = sno;
	}
	public Integer getScheNo() {
		return scheNo;
	}
	public void setScheNo(Integer scheNo) {
		this.scheNo = scheNo;
	}
	
	
}
