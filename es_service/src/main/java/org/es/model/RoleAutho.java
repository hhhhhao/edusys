package org.es.model;

public class RoleAutho {
    private Integer no;

    private Integer rolNo;

    private Integer authNo;

    public Integer getNo() {
        return no;
    }

    public void setNo(Integer no) {
        this.no = no;
    }

    public Integer getRolNo() {
        return rolNo;
    }

    public void setRolNo(Integer rolNo) {
        this.rolNo = rolNo;
    }

    public Integer getAuthNo() {
        return authNo;
    }

    public void setAuthNo(Integer authNo) {
        this.authNo = authNo;
    }
}